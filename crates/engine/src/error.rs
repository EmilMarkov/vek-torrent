//! Ошибки встроенного торрент-движка.

/// Ошибка операции движка.
#[derive(Debug, thiserror::Error)]
pub enum Error {
    /// Не удалось инициализировать сессию движка.
    #[error("не удалось запустить торрент-движок: {0}")]
    Start(String),

    /// Некорректный источник (magnet/ссылка/файл).
    #[error("некорректный источник торрента: {0}")]
    InvalidSource(String),

    /// Торрент с указанным хэшем не найден.
    #[error("торрент не найден")]
    NotFound,

    /// Ошибка операции библиотеки librqbit.
    #[error("ошибка движка: {0}")]
    Backend(String),
}

/// Результат операций движка.
pub type Result<T> = std::result::Result<T, Error>;
